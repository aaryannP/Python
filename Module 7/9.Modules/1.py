# Write a Python program to import the math module and use functions like sqrt(), ceil(),  floor().

import math

num = int(input("Enter the number what you want to square root : "))
print("Square root of number : ",math.sqrt(num))

num = float(input("Enter the number : "))
print("Ceil value of number : ",math.ceil(num))
print("Flloor value of number : ",math.floor(num))